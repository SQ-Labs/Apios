<h1>Projet Asclépios</h1>
<h2>SQ Labs</h2>
<p>Ethique et innovante, SQ Labs vous propose une solution bipartite de gestion et traitement des données des patients. Notre but ? Contribuer à réduire les antiflux dans les hôpitaux.</p>
<h2>Apios</h2>
<p>Un CRM sécurisé avec une interface pour les professionnels de santé et une interface pour les patients. Lors de l'admission d'un patient, ses données et symptômes y sont renseignées afin de constituer un dossier médical. De son côté, le patient peut ajouter des informations et diagnostics antérieurs pour étayer son dossier. (ex. prise de sang, radiographies, etc.).</p>
<h2>Clépios</h2>
<p>Un algorithme de chiffrement des données et d'aide au diagnostic, Clépios permet dans un premier temps l'anonymisation des données renseignées et, grâce à un chiffrement homomorphique, permet une aide au diagnostic : proposer des pistes immédiates quant à l'admission du patient, et anticiper les prédispositions à d'autres diagnostic.</p>